
import numpy as np

#the square root of a number
print(np.sqrt(100))

#the absolute value of a number
print(np.abs(-9))

#log base 10 of a number
print(np.log10(100))

#cos of a number in radians
print(np.cos(0))

#arcsin of a number in radians
print(np.arcsin(1))

#exponential of a number
print(np.exp(1))

#sign of a number (returns +1 or -1)
print(np.sign(5))

#sign of another number (returns +1 or -1)
print(np.sign(-5))

#round a number
print(np.rint(8.1))

#ceiling of a number
print(np.ceil(8.1))


